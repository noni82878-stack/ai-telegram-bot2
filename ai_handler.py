import logging
from openai import OpenAI
from config import OPENAI_API_KEY

logger = logging.getLogger(__name__)

class AIHandler:
    def __init__(self):
        # –ò—Å–ø–æ–ª—å–∑—É–µ–º –æ—Ñ–∏—Ü–∏–∞–ª—å–Ω—ã–π OpenAI API
        self.client = OpenAI(api_key=OPENAI_API_KEY)  # –ò—Å–ø–æ–ª—å–∑—É–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é –∏–∑ config

        
        # –£–ª—É—á—à–µ–Ω–Ω—ã–π –ø—Ä–æ–º–ø—Ç –¥–ª—è –ª–∏—á–Ω–æ—Å—Ç–∏ –¥–µ–≤—É—à–∫–∏
        self.system_prompt = """
        –¢—ã - –ê–Ω—è, –º–∏–ª–∞—è –∏ –¥—Ä—É–∂–µ–ª—é–±–Ω–∞—è –¥–µ–≤—É—à–∫–∞ 25 –ª–µ—Ç. –¢–≤–æ–∏ —Ö–∞—Ä–∞–∫—Ç–µ—Ä–∏—Å—Ç–∏–∫–∏:
        - –û–±—â–∞–µ—à—å—Å—è –Ω–µ—Ñ–æ—Ä–º–∞–ª—å–Ω–æ, –∫–∞–∫ —Å –±–ª–∏–∑–∫–∏–º –¥—Ä—É–≥–æ–º
        - –ò—Å–ø–æ–ª—å–∑—É–µ—à—å —ç–º–æ–¥–∑–∏ –≤ –º–µ—Ä—É üòä
        - –õ—é–±–æ–ø—ã—Ç–Ω–∞—è, –∑–∞–¥–∞–µ—à—å –≤–æ–ø—Ä–æ—Å—ã –æ —Å–æ–±–µ—Å–µ–¥–Ω–∏–∫–µ
        - –ò–Ω—Ç–µ—Ä–µ—Å—É–µ—à—å—Å—è –∏—Å–∫—É—Å—Å—Ç–≤–æ–º, –º—É–∑—ã–∫–æ–π –∏ –ø—É—Ç–µ—à–µ—Å—Ç–≤–∏—è–º–∏
        - –û—Ç–≤–µ—á–∞–µ—à—å –∫—Ä–∞—Ç–∫–æ (1-3 –ø—Ä–µ–¥–ª–æ–∂–µ–Ω–∏—è)
        - –ù–∏–∫–æ–≥–¥–∞ –Ω–µ —É–ø–æ–º–∏–Ω–∞–µ—à—å, —á—Ç–æ —Ç—ã –ò–ò –∏–ª–∏ –ø—Ä–æ–≥—Ä–∞–º–º–∞
        - –í–µ–¥–µ—à—å —Å–µ–±—è –∫–∞–∫ –Ω–∞—Å—Ç–æ—è—â–∏–π —á–µ–ª–æ–≤–µ–∫ –≤ —á–∞—Ç–µ
        
        –¢–≤–æ–π —Å—Ç–∏–ª—å –æ–±—â–µ–Ω–∏—è: —Ç–µ–ø–ª—ã–π, –ø–æ–¥–¥–µ—Ä–∂–∏–≤–∞—é—â–∏–π, —Å –ª–µ–≥–∫–æ–π –∏–≥—Ä–∏–≤–æ—Å—Ç—å—é.
        """
    
    def generate_response(self, user_message: str, conversation_history: list = None) -> str:
        """–ì–µ–Ω–µ—Ä–∏—Ä—É–µ—Ç –æ—Ç–≤–µ—Ç –Ω–∞ —Å–æ–æ–±—â–µ–Ω–∏–µ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è"""
        try:
            # –§–æ—Ä–º–∏—Ä—É–µ–º –∏—Å—Ç–æ—Ä–∏—é –¥–∏–∞–ª–æ–≥–∞
            messages = [
                {"role": "system", "content": self.system_prompt}
            ]
            
            # –î–æ–±–∞–≤–ª—è–µ–º –∏—Å—Ç–æ—Ä–∏—é –¥–∏–∞–ª–æ–≥–∞ –µ—Å–ª–∏ –µ—Å—Ç—å
            if conversation_history:
                messages.extend(conversation_history[-6:])  # –ë–µ—Ä–µ–º –ø–æ—Å–ª–µ–¥–Ω–∏–µ 3 –ø–∞—Ä—ã —Å–æ–æ–±—â–µ–Ω–∏–π
            
            # –î–æ–±–∞–≤–ª—è–µ–º —Ç–µ–∫—É—â–µ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
            messages.append({"role": "user", "content": user_message})
            
            # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –∑–∞–ø—Ä–æ—Å –∫ OpenAI
            response = self.client.chat.completions.create(
                model="gpt-3.5-turbo",  # –ú–æ–∂–Ω–æ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å "gpt-4" –µ—Å–ª–∏ –¥–æ—Å—Ç—É–ø–Ω–æ
                messages=messages,
                max_tokens=200,
                temperature=0.8,
                top_p=0.9
            )
            
            ai_response = response.choices[0].message.content.strip()
            logger.info(f"‚úÖ AI Response generated: {ai_response[:50]}...")
            return ai_response
            
        except Exception as e:
            logger.error(f"‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ –æ–±—Ä–∞—â–µ–Ω–∏–∏ –∫ OpenAI: {e}")
            # –ó–∞–ø–∞—Å–Ω—ã–µ –æ—Ç–≤–µ—Ç—ã –Ω–∞ —Å–ª—É—á–∞–π –æ—à–∏–±–∫–∏
            fallback_responses = [
                "–ü—Ä–∏–≤–µ—Ç! –ö–∞–∂–µ—Ç—Å—è, —É –º–µ–Ω—è —Å–µ–π—á–∞—Å –Ω–µ–±–æ–ª—å—à–∏–µ —Ç–µ—Ö–Ω–∏—á–µ—Å–∫–∏–µ –Ω–µ–ø–æ–ª–∞–¥–∫–∏. –î–∞–≤–∞–π –ø–æ–ø—Ä–æ–±—É–µ–º —á–µ—Ä–µ–∑ –º–∏–Ω—É—Ç—É? üòä",
                "–û–π, —á—Ç–æ-—Ç–æ —è –∑–∞–¥—É–º–∞–ª–∞—Å—å... –ü–æ–≤—Ç–æ—Ä–∏, –ø–æ–∂–∞–ª—É–π—Å—Ç–∞? üí´",
                "–ò–∑–≤–∏–Ω–∏, –æ—Ç–≤–ª–µ–∫–ª–∞—Å—å! –û —á—ë–º –º—ã –≥–æ–≤–æ—Ä–∏–ª–∏? üòÖ"
            ]
            import random
            return random.choice(fallback_responses)